import numpy as np
import matplotlib.pyplot as plt

def f(a1,b1,x1,y1):
    return np.add(np.multiply(np.subtract(a1,x1),np.subtract(a1,x1)),b1*np.subtract(y1,x1*x1)*np.subtract(y1,x1*x1))

